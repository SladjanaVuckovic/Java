
public class Primer2 {

	public static void main(String[] args) {

		int i = 0;
		System.out.println("Uvecaj pa poredi:");
		while (++i < 5) {
			// uvećaj pa poredi
			System.out.println(i); // ispis uvećane vrednosti za i
		}

		System.out.println("Poredi pa uvećaj");
		int j = 0;
		while (j++ < 5) {
			// poredi pa uvećaj
			System.out.println(j); // ispis uvećane vrednosti za i
		}

	}

}
