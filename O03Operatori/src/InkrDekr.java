public class InkrDekr {

	public static void main(String[] args) {

		// Operatori inkrementerinja i dekrementiranja

		int i = 10;

		i += 1; // primena operatora inkrementiranja
		System.out.println(i);

		/*
		 * i++ i += 1 i = i + 1
		 * 
		 */

		// Operator dekrementiranja (umenjanja vrednosti za 1)

		float b = 20.56f;
		b--; // -- prestavljaju operator dekrementiranja
		// b = b - 1;
		System.out.println(b);

	}

}
