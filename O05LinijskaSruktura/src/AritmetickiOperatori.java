/*Napisati program za testiranje aritmetiÄkih operatora +, -, *, / i % nad operandima 2 i 5.*/

public class AritmetickiOperatori {
	public static void main(String[] args) {
		
		System.out.println("5 + 2 = " + (5 + 2));
		System.out.println("5 - 2 = " + (5 - 2));
		System.out.println("5 * 2 = " + 5 * 2);
		System.out.println("5 / 2 = " + 5 / 2);
		System.out.println("5.0 / 2.0 = " + 5.0 / 2.0);
		System.out.println("5 % 2 = " + 5 % 2);
	}
}