
public class Primer1 {
	
	public static void main(String[] args) {

		// ab+ac

		int a = 5, b = 10, c = 24, d;
		d = a * b + a * c;

		System.out.println(d);

		double e = 3 * (2 * a - 8 * b) / c;

		System.out.println(e);

		double f = (a + b) * (3 * c + 1) / (c - 3);

		System.out.println(f);

	}

}


